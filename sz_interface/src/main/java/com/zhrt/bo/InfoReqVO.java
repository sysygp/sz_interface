package com.zhrt.bo;


public class InfoReqVO{

	private String channelapp;
	private String reqIp;
	private String imei;
	private String imsi;
	private String uuid;
	private String version;
	private String factory;
	private String safeSoft;
	
	
	
	public String getChannelapp() {
		return channelapp;
	}
	public void setChannelapp(String channelapp) {
		this.channelapp = channelapp;
	}
	public String getImei() {
		return imei;
	}
	public void setImei(String imei) {
		this.imei = imei;
	}
	public String getImsi() {
		return imsi;
	}
	public void setImsi(String imsi) {
		this.imsi = imsi;
	}
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getFactory() {
		return factory;
	}
	public void setFactory(String factory) {
		this.factory = factory;
	}
	public String getSafeSoft() {
		return safeSoft;
	}
	public void setSafeSoft(String safeSoft) {
		this.safeSoft = safeSoft;
	}
	public String getReqIp() {
		return reqIp;
	}
	public void setReqIp(String reqIp) {
		this.reqIp = reqIp;
	}
	
}
