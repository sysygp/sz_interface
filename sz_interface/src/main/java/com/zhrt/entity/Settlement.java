package com.zhrt.entity;

public class Settlement {
	
	private String id;
	private String channelId;
	private String channelAppId;
	private String appId;
	private String settPercent;//接榫百分比
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getChannelId() {
		return channelId;
	}
	public void setChannelId(String channelId) {
		this.channelId = channelId;
	}
	public String getChannelAppId() {
		return channelAppId;
	}
	public void setChannelAppId(String channelAppId) {
		this.channelAppId = channelAppId;
	}
	public String getAppId() {
		return appId;
	}
	public void setAppId(String appId) {
		this.appId = appId;
	}
	public String getSettPercent() {
		return settPercent;
	}
	public void setSettPercent(String settPercent) {
		this.settPercent = settPercent;
	}
	
	

}
