package com.zhrt.entity;

public class SpcodeConvert {
	
	private String id;
	private String spCodeId;
	private String convertDay;//日转化
	private String convertMonth;//月转化
	private String convertAll;//总转化
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSpCodeId() {
		return spCodeId;
	}
	public void setSpCodeId(String spCodeId) {
		this.spCodeId = spCodeId;
	}
	public String getConvertDay() {
		return convertDay;
	}
	public void setConvertDay(String convertDay) {
		this.convertDay = convertDay;
	}
	public String getConvertMonth() {
		return convertMonth;
	}
	public void setConvertMonth(String convertMonth) {
		this.convertMonth = convertMonth;
	}
	public String getConvertAll() {
		return convertAll;
	}
	public void setConvertAll(String convertAll) {
		this.convertAll = convertAll;
	}

	
}
